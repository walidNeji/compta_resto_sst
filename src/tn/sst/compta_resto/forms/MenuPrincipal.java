    private void compta_grandLivre_RechercheActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_compta_grandLivre_RechercheActionPerformed
        // TODO add your handling code here:
        String type = "";
        DecimalFormat decimalFormat = new DecimalFormat("###,###.###");
        Object stringArray[] = {"Directe", "Indirecte", "Les Deux"};
        int choix2 = JOptionPane.showOptionDialog(null, "Quel Type vous les vous afficher ? ", "TYPE GRAND LIVRE",
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, stringArray,
                stringArray[0]);
        if (choix2 == 0) {
            type = "DIRECTE";
        } else if (choix2 == 1) {
            type = "INDIRECTE";
        }
        while (table_GL.getRowCount() > 0) {
            ((DefaultTableModel) table_GL.getModel()).removeRow(0);
        }
        FichVenteJpaController fvjc = new FichVenteJpaController();
        FichRecetteDepJpaController depJpaController = new FichRecetteDepJpaController();
        String[] dates_interval = MyDate.listeDesDates(jXDatePicker1.getDate(), jXDatePicker2.getDate()).split(";");
        float dep_cumul = 0;
        for (int i = 0; i < dates_interval.length; i++) {
            String date = dates_interval[i];
            Date date_courante = MyDate.formatStringToDate(date);
            if (date_courante != null) {
                switch (type) {
                    case "DIRECTE": {
                        float vente = fvjc.getVentesTotalByDate(date_courante, type);
                        float depense = depJpaController.getDepenseTotalByDate(date_courante, type);
                        dep_cumul += vente - depense;
                        if (vente != 0 || depense != 0) {
                            Object[] obj = new Object[]{date, decimalFormat.format(vente), "0", "0", decimalFormat.format(depense), decimalFormat.format(vente), "0", decimalFormat.format(vente - depense), decimalFormat.format(dep_cumul)};
                            ((DefaultTableModel) table_GL.getModel()).insertRow(table_GL.getRowCount(), obj);
                        }
                        break;
                    }
                    case "INDIRECTE": {
                        float vente = fvjc.getVentesTotalByDate(date_courante, type);
                        float depense = depJpaController.getDepenseTotalByDate(date_courante, type);
                        dep_cumul += vente - depense;
                        if (vente != 0 || depense != 0) {
                            Object[] obj = new Object[]{date, "0", decimalFormat.format(vente), decimalFormat.format(depense), "0", "0", decimalFormat.format(depense), decimalFormat.format(vente - depense), decimalFormat.format(dep_cumul)};
                            ((DefaultTableModel) table_GL.getModel()).insertRow(table_GL.getRowCount(), obj);
                        }
                        break;
                    }
                    case "": {
                        float vente_directe = fvjc.getVentesTotalByDate(date_courante, "DIRECTE");
                        float vente_indirecte = fvjc.getVentesTotalByDate(date_courante, "INDIRECTE");

                        float depense_directe = depJpaController.getDepenseTotalByDate(date_courante, "DIRECTE");
                        float depense_indirecte = depJpaController.getDepenseTotalByDate(date_courante, "INDIRECTE");

                        dep_cumul += (vente_directe + vente_indirecte) - (depense_directe + depense_indirecte);
                        if (vente_directe != 0 || depense_directe != 0 || depense_indirecte != 0 || vente_indirecte != 0) {
                            Object[] obj = new Object[]{date, decimalFormat.format(vente_directe), decimalFormat.format(vente_indirecte),
                                decimalFormat.format(depense_indirecte), decimalFormat.format(depense_directe),
                                decimalFormat.format(vente_directe + vente_indirecte),
                                decimalFormat.format(depense_directe + depense_indirecte),
                                decimalFormat.format((vente_directe + vente_indirecte) - (depense_directe + depense_indirecte)),
                                decimalFormat.format(dep_cumul)};
                            ((DefaultTableModel) table_GL.getModel()).insertRow(table_GL.getRowCount(), obj);
                        }
                        break;
                    }
                }

            }
        }
    }//GEN-LAST:event_compta_grandLivre_RechercheActionPerformed

