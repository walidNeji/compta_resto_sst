    public void articlesAfficheDetails() {

        tn.sst.compta_resto.dao.FichProduitJpaController fcjc = new tn.sst.compta_resto.dao.FichProduitJpaController();
        if (articles_combo_names.getSelectedItem() != null) {
            tn.sst.compta_resto.pojos.FichProduit fr = fcjc.findByNom(articles_combo_names.getSelectedItem().toString());
            if (fr != null) {
                articles_pa.setText(fr.getPrixAchat() + "");
                articles_pv.setText(fr.getPrixVente() + "");
                articles_familles_names.removeAllItems();
                articles_familles_names.addItem(fr.getClefamille().getLibelle());
            }
        }

    }

