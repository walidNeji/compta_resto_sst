    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        code = jTextField1.getText();
        mdp = jPasswordField1.getText();
        //  System.out.println(code + "  " + mdp);
        query = entityManager.createQuery("SELECT u FROM FichUtil u where u.codut= :codut and u.passut = :passut");
        query.setParameter("codut", code);
        query.setParameter("passut", mdp);
        list = query.getResultList();
        //System.out.println(list.size());
        if (list.size() > 0) {
            FichUtil util = list.get(0);
            profil = util.getProfil();
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Utilisateur ou Mot de passe invalide !", "Message d'erreur", JOptionPane.ERROR_MESSAGE);
            jTextField1.setText("");
            jPasswordField1.setText("");
        }
}//GEN-LAST:event_jButton1ActionPerformed

    private void jPasswordField1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jPasswordField1KeyPressed
        // TODO add your handling code here:
        if (evt.getSource().equals(jPasswordField1)) {
            if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
                code = jTextField1.getText();
                mdp = jPasswordField1.getText();
                //  System.out.println(code + "  " + mdp);
                query = entityManager.createQuery("SELECT u FROM FichUtil u where u.codut= :codut and u.passut = :passut");
                query.setParameter("codut", code);
                query.setParameter("passut", mdp);
                list = query.getResultList();
                //System.out.println(list.size());
                if (list.size() > 0) {
                    FichUtil util = list.get(0);
                    profil = util.getProfil();
                    dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Utilisateur ou Mot de passe invalide !", "Message d'erreur", JOptionPane.ERROR_MESSAGE);
                    jTextField1.setText("");
                    jPasswordField1.setText("");
                }
            }
        }

    }//GEN-LAST:event_jPasswordField1KeyPressed

